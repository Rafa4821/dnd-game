/**
 * Buscar enemigo por ID
 */
export function getEnemyById(id: string): Enemy | undefined {
  return BESTIARY[id]
}

/**
 * Filtrar enemigos por CR
 */
export function getEnemiesByCR(minCR: number, maxCR: number): Enemy[] {
  return Object.values(BESTIARY).filter(
    enemy => enemy.cr >= minCR && enemy.cr <= maxCR
  )
}

/**
 * Filtrar enemigos por rol
 */
export function getEnemiesByRole(role: string): Enemy[] {
  return Object.values(BESTIARY).filter(enemy => enemy.role === role)
}
